for x in range(5): # cria um loop de 0 a 5, retorna em cada linha 
    print(x) # cada linha: 0, 1, 2, 3, 4

lista = [11,12,13,14,15]

for z in range(5): # mesma coisa acima, retorna em cada linha o numero dos elemento da lista
    print(z) # cada linha: 0, 1, 2, 3, 4

for z in lista: # vai mostrar cada elemento da lista por linha
    print(z) # cada linha: 11, 12, 13, 14, 15
