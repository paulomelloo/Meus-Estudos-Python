"""
Objetivo: Usar o loop while para fazer uma contagem regressiva de 10 até 0.
Inicialização: Crie uma variável contador e inicialize-a com o valor 10.
Repetição: Use um loop while que continue executando enquanto o contador for maior ou igual a 0.
Ação: Dentro do loop, imprima o valor atual do contador.
Atualização: Decremente o valor do contador em 1 a cada repetição.
Finalização: Após o loop terminar, imprima a mensagem: "Lançar!"
"""

x = 10
y=0

while y <= x:
    print(x)
    x = x - 1
print(f"Lançar!")
