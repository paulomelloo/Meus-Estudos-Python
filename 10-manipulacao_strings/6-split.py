itens1 = "maçaneta, porta, geladeira, carro, caminhao, cachorra" # cria a variavel string

lista = itens1.split(", ") # vai criar uma lista, separando as palavras da string por ', '.
print(lista) # ira retornar: ['maçaneta', 'porta', 'geladeira', 'carro', 'caminhao', 'cachorra']

